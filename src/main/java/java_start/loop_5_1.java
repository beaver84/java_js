package java_start;

public class loop_5_1 {
    public static void main(String[] args) {
        int count = 2;
        while(count < 21){
            System.out.println(count);
            count = count + 2;
        }

        for (int count2 = 2; count2 < 21; count2 = count2 + 2){
            System.out.println(count2);
        }
    }

}
